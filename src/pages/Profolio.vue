        <div id="About">
            <img id="Avatar" :src="avatar" />
            <h1 id="Stroke">{{ title }}</h1>
            <h2 id="Info" v-for="(item, idx) in info" :key="idx">{{ item }}</h2>
        </div>

        <cover-img />

        <div id="Repo" v-for="(repo, idx) in repos" :key="idx">
            ðŸ“š {{ repo.name }} {{ repo.stargazers_count }}
